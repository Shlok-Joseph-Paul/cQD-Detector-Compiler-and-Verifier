import { serializeCsv } from "./csv.ts";
import type { JoinedMeasurement } from "./types.ts";

export const ATLAS_EXPORT_COLUMNS = [
  "measurement_id",
  "paper_id",
  "device_id",
  "material_family",
  "material_composition",
  "device_architecture",
  "device_stack",
  "active_area_cm2",
  "wavelength_nm",
  "detectivity_jones",
  "responsivity_a_w",
  "eqe_percent",
  "temperature_k",
  "bias_v",
  "measurement_frequency_hz",
  "response_time_s",
  "bandwidth_hz",
  "noise_method",
  "detectivity_extraction_method",
  "source_location",
  "curator_status",
  "flag",
  "amber_reasons",
  "amber_explanation",
  "curator_notes",
  "date_added",
  "date_updated",
  "paper_title",
  "authors",
  "first_author",
  "journal",
  "publication_year",
  "doi",
  "publication_url",
  "publication_type",
  "peer_reviewed",
] as const;

export function exportJoinedMeasurementsCsv(
  records: readonly JoinedMeasurement[],
): string {
  return serializeCsv(
    ATLAS_EXPORT_COLUMNS,
    records.map(({ paper, device, measurement }) => [
      measurement.measurement_id,
      paper.paper_id,
      device.device_id,
      device.material_family,
      device.material_composition,
      device.device_architecture,
      device.device_stack,
      device.active_area_cm2,
      measurement.wavelength_nm,
      measurement.detectivity_jones,
      measurement.responsivity_a_w,
      measurement.eqe_percent,
      measurement.temperature_k,
      measurement.bias_v,
      measurement.measurement_frequency_hz,
      measurement.response_time_s,
      measurement.bandwidth_hz,
      measurement.noise_method,
      measurement.detectivity_extraction_method,
      measurement.source_location,
      measurement.curator_status,
      measurement.flag,
      measurement.amber_reasons.join("|"),
      measurement.amber_explanation,
      measurement.curator_notes,
      measurement.date_added,
      measurement.date_updated,
      paper.title,
      paper.authors.join("|"),
      paper.first_author,
      paper.journal,
      paper.publication_year,
      paper.doi,
      paper.publication_url,
      paper.publication_type,
      paper.peer_reviewed,
    ]),
  );
}
